## Cronograma recomendado (3 semanas antes → dia do evento)

- **Semana -3**: briefing pronto, arte finalizada, evento cadastrado no AgendaAqui, ingressos abertos.
- **Semana -2**: primeiro disparo nos grupos de WhatsApp, post de anúncio no Instagram, cartazes distribuídos.
- **Semana -1**: parcerias com influenciadores publicando, segundo disparo no WhatsApp, stories diários.
- **Véspera**: mensagem "É amanhã!" nos grupos, story com contagem regressiva, confirmação por WhatsApp para quem comprou ingresso.
- **Dia do evento**: story ao vivo da montagem, "portas abertas", vídeos curtos da chegada do público — isso puxa quem estava em dúvida.

## Boas práticas que dobram a taxa de comparecimento

- **Um link único** para inscrição/ingresso (nada de "manda mensagem que te passo o pix").
- **Confirmação por WhatsApp na véspera** para quem comprou — reduz no-show em até 30%.
- **Cartaz com endereço e ponto de referência** (não só "Rua X, 100" — diga "em frente à padaria Y").
- **Fotos e vídeos de edições anteriores** geram prova social.
- **Facilidade de pagamento**: aceite Pix, cartão e dinheiro na porta.
- **Comunique acessibilidade e estacionamento** com antecedência.

## Erros comuns que esvaziam eventos

- Começar a divulgar com menos de 10 dias — em Vespasiano a agenda das famílias fecha rápido.
- Não responder mensagens em até 2 horas — quem pergunta e não recebe resposta desiste.
- Arte poluída, com mais de 20 palavras ou fonte fina difícil de ler no celular.
- Endereço confuso, sem link do Google Maps.
- Ignorar acessibilidade, estacionamento e transporte público na comunicação.
- Não fazer pós-evento (agradecimento e teaser da próxima edição) — você perde a base para a próxima.

