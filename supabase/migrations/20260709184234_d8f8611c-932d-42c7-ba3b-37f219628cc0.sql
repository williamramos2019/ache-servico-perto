## Antes de divulgar: o briefing de 5 minutos

Antes de qualquer post, responda por escrito:

- **O quê**: nome do evento em uma frase (ex.: "Noite do Samba com Grupo Raiz").
- **Quando**: data, horário de abertura e horário do atração principal.
- **Onde**: endereço completo + ponto de referência + link do Google Maps.
- **Quanto**: valor do ingresso, se é solidário (1kg de alimento) ou gratuito.
- **Para quem**: público-alvo (famílias, jovens 18-30, terceira idade, empresários).
- **CTA**: qual é a ÚNICA ação que a pessoa deve tomar? (comprar ingresso, confirmar presença, salvar data).

Se você não consegue responder essas 6 perguntas em 5 minutos, seu público também não vai entender o convite.

## Os 7 canais que funcionam em Vespasiano (por ordem de retorno)

### 1. WhatsApp — o campeão local
Grupos de bairro (Cristina, Nova Pampulha, Centro, Caieiras, Jardim Santa Cruz, Marimbá) concentram o público que efetivamente comparece. Estratégia:

- Prepare um **card único** (1080x1350px) e uma **mensagem curta** com data, hora, local e link.
- Peça para 5 amigos-âncora encaminharem — não faça spam pessoal em massa.
- Crie uma **lista de transmissão** com quem já foi em eventos anteriores.
- Envie novamente 24h antes com "É hoje!" e o endereço.

### 2. Instagram local com geolocalização
- Marque a localização **Vespasiano - MG** ou **São José da Lapa - MG** em todos os posts e stories.
- Use hashtags regionais: #vespasiano #vespasianomg #saojosedalapa #agendaaquivespasiano #eventosvespasiano.
- Poste 3 vezes: anúncio (2-3 semanas antes), lembrete (1 semana) e contagem regressiva (24h).
- Stories com enquete ("Vai?") aumentam alcance orgânico em 40-70%.

### 3. AgendaAqui — vitrine da cidade
Cadastre seu evento em [/eventos](/eventos). É gratuito e aparece para quem já está pesquisando "o que fazer em Vespasiano hoje". Vantagens:

- SEO local (aparece no Google quando alguém busca pela cidade).
- Integração com favoritos: quem salva recebe lembrete.
- Página com endereço, mapa, programação e link direto para ingressos.

### 4. Google Meu Negócio / Google Maps
Se você tem um espaço fixo (bar, salão, igreja, restaurante), publique o evento como **post no Google Meu Negócio**. É gratuito e aparece quando alguém pesquisa seu nome ou "eventos hoje perto de mim".

### 5. Cartaz físico bem colocado
Ainda funciona muito em bairros residenciais. Regra do "3 metros": o cartaz precisa ter data, hora e local **legíveis a 3 metros de distância**. Pontos que valem: padarias, salões de beleza, mercadinhos, UBSs, escolas (com autorização), pontos de ônibus.

### 6. Parcerias cruzadas
Ofereça permuta para influenciadores locais (500 a 5.000 seguidores em Vespasiano/S.J. da Lapa) — 2 convites em troca de 1 post + 3 stories. O micro-influenciador local converte muito mais que celebridade nacional.

### 7. Anúncio pago (opcional)
